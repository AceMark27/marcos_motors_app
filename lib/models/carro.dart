class Carro {
  final String id;
  final String nome;
  final String descricao;
  final String imagemPath;
  final double preco;
  final String cor;
  final int ano;
  final String combustivel;
  final String transmissao;
  final int kilometragem;
  final String outros;

  Carro({
    required this.id,
    required this.nome,
    required this.descricao,
    required this.imagemPath,
    required this.preco,
    required this.cor,
    required this.ano,
    required this.combustivel,
    required this.transmissao,
    required this.kilometragem,
    required this.outros,
  });
}
